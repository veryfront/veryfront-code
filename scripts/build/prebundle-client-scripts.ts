#!/usr/bin/env -S deno run --allow-all
/**
 * Pre-bundle Client Scripts for Compiled Binary
 *
 * In `deno compile` builds, framework source files (src/rendering/client/*)
 * aren't embedded in the binary. This script bundles the client-side router
 * and prefetch scripts ahead of time and writes them into templates.ts so
 * the existing import in client-runtime.ts picks them up automatically.
 *
 * Runs as part of `deno task build`, before `deno compile`.
 */

import { dirname, fromFileUrl, join } from "#std/path.ts";
import {
  generateClientModule,
  generatePrefetchScript,
} from "../../src/build/production-build/client-runtime.ts";
import { CLIENT_STYLES } from "../../src/build/production-build/templates.ts";

const scriptDir = dirname(fromFileUrl(import.meta.url));
const projectRoot = join(scriptDir, "..", "..");
const templatesPath = join(
  projectRoot,
  "src",
  "build",
  "production-build",
  "templates.ts",
);

console.log("[prebundle-client-scripts] Bundling client router...");
const routerBundle = await generateClientModule();

console.log("[prebundle-client-scripts] Bundling client prefetch...");
// deno-lint-ignore no-explicit-any
const prefetchBundle = await generatePrefetchScript(null as any);

const output = `/**
 * Embedded templates for production builds
 * These are embedded as strings to avoid file system dependencies in npm bundle
 *
 * AUTO-GENERATED by scripts/build/prebundle-client-scripts.ts
 * Do not edit manually — run \`deno task build\` to regenerate.
 * @module
 */

export const CLIENT_STYLES = ${JSON.stringify(CLIENT_STYLES)};

export const CLIENT_ROUTER_BUNDLE: string | undefined = ${JSON.stringify(routerBundle)};

export const CLIENT_PREFETCH_BUNDLE: string | undefined = ${JSON.stringify(prefetchBundle)};
`;

await Deno.writeTextFile(templatesPath, output);
console.log(`[prebundle-client-scripts] Written to ${templatesPath}`);
