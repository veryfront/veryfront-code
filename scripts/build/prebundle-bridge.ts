#!/usr/bin/env -S deno run --allow-all
/**
 * Pre-bundle Studio Bridge for Compiled Binary
 *
 * In `deno compile` builds, the bridge source files (src/studio/bridge/*)
 * aren't accessible via the filesystem. This script bundles them into a
 * single JS string at build time and writes it into bridge-bundle.generated.ts
 * so the handler can import it directly.
 *
 * Runs as part of `deno task build`, before `deno compile`.
 */

import { dirname, fromFileUrl, join } from "#std/path.ts";
import { EsbuildBundler } from "../../extensions/ext-bundler-esbuild/src/esbuild-bundler.ts";

const scriptDir = dirname(fromFileUrl(import.meta.url));
const projectRoot = join(scriptDir, "..", "..");
const outputRelativePath = "src/studio/bridge/bridge-bundle.generated.ts";
const outputPath = join(projectRoot, outputRelativePath);
const entryPoint = "src/studio/bridge/bridge-coordinator.ts";
const emptyBundleErrorMessage = "esbuild produced empty output";
const bundler = new EsbuildBundler();

/** Build the production Studio bridge bundle without writing generated files. */
export async function buildStudioBridgeBundle(): Promise<string> {
  const { outputFiles } = await bundler.bundle({
    absWorkingDir: projectRoot,
    bundle: true,
    write: false,
    format: "esm",
    platform: "browser",
    target: "es2022",
    entryPoints: [entryPoint],
  });

  const js = outputFiles?.[0]?.text ?? "";
  if (!js) {
    throw new Error(emptyBundleErrorMessage);
  }

  return js;
}

/** Stop the build helper and its owned subprocess. */
export function stopStudioBridgeBundler(): Promise<void> {
  return bundler.stop();
}

async function main(): Promise<void> {
  console.log("[prebundle-bridge] Bundling studio bridge modules...");
  const js = await buildStudioBridgeBundle();

  const output = `/**
 * Pre-bundled Studio Bridge Script
 *
 * AUTO-GENERATED by scripts/build/prebundle-bridge.ts
 * Do not edit manually - run \`deno task build\` to regenerate.
 * @module
 */

// deno-fmt-ignore - keep the bundled script as a generated JSON string.
export const STUDIO_BRIDGE_BUNDLE: string = ${JSON.stringify(js)};
`;

  await Deno.writeTextFile(outputPath, output);
  console.log(
    `[prebundle-bridge] Written to ${outputRelativePath} (${js.length} bytes)`,
  );
}

if (import.meta.main) {
  try {
    await main();
  } catch (error) {
    if (error instanceof Error && error.message === emptyBundleErrorMessage) {
      console.error(`[prebundle-bridge] ERROR: ${emptyBundleErrorMessage}`);
      Deno.exit(1);
    }
    throw error;
  }
}
