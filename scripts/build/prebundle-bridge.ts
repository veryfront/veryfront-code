#!/usr/bin/env -S deno run --allow-all
/**
 * Pre-bundle Studio Bridge for Compiled Binary
 *
 * In `deno compile` builds, the bridge source files (src/studio/bridge/*)
 * aren't accessible via the filesystem. This script bundles them into a
 * single JS string at build time and writes it into bridge-bundle.generated.ts
 * so the handler can import it directly.
 *
 * Runs as part of `deno task build`, before `deno compile`.
 */

import { dirname, fromFileUrl, join } from "#std/path.ts";

const scriptDir = dirname(fromFileUrl(import.meta.url));
const projectRoot = join(scriptDir, "..", "..");
const bridgeDir = join(projectRoot, "src", "studio", "bridge");
const entryPoint = join(bridgeDir, "bridge-coordinator.ts");
const outputPath = join(bridgeDir, "bridge-bundle.generated.ts");

console.log("[prebundle-bridge] Bundling studio bridge modules...");

const { build } = await import("esbuild");
const { outputFiles } = await build({
  bundle: true,
  write: false,
  format: "esm",
  platform: "browser",
  target: "es2022",
  entryPoints: [entryPoint],
});

const js = outputFiles?.[0]?.text ?? "";

if (!js) {
  console.error("[prebundle-bridge] ERROR: esbuild produced empty output");
  Deno.exit(1);
}

const output = `/**
 * Pre-bundled Studio Bridge Script
 *
 * AUTO-GENERATED by scripts/build/prebundle-bridge.ts
 * Do not edit manually — run \`deno task build\` to regenerate.
 * @module
 */

export const STUDIO_BRIDGE_BUNDLE: string = ${JSON.stringify(js)};
`;

await Deno.writeTextFile(outputPath, output);
console.log(`[prebundle-bridge] Written to ${outputPath} (${js.length} bytes)`);
