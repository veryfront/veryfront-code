/**
 * Generates src/integrations/_connectors.ts and src/integrations/_icons.ts.
 *
 * Reads all cli/templates/integrations/<name>/connector.json and *.svg files,
 * skips _base (internal), strips callbackPath from auth, and writes typed ReadonlyMaps.
 *
 * Usage:
 *   deno run -A scripts/build/generate-integrations-module.ts
 */

interface ConnectorJson {
  name: string;
  icon?: string;
  internal?: boolean;
  auth?: { callbackPath?: string; [key: string]: unknown };
  [key: string]: unknown;
}

const integrationsDir = "./cli/templates/integrations";
const connectorsPath = "./src/integrations/_connectors.ts";
const iconsPath = "./src/integrations/_icons.ts";

const connectorEntries: [string, ConnectorJson][] = [];
const iconEntries: [string, string][] = [];

for await (const entry of Deno.readDir(integrationsDir)) {
  if (!entry.isDirectory) continue;
  if (entry.name === "_base") continue;

  const dirPath = `${integrationsDir}/${entry.name}`;
  const connectorPath = `${dirPath}/connector.json`;

  try {
    const raw = await Deno.readTextFile(connectorPath);
    const connector: ConnectorJson = JSON.parse(raw);

    // Skip internal connectors
    if (connector.internal) continue;

    // Strip callbackPath from auth
    if (connector.auth) {
      const { callbackPath: _, ...authWithout } = connector.auth;
      connector.auth = authWithout;
    }

    connectorEntries.push([connector.name, connector]);

    // Read SVG icon if referenced
    if (connector.icon) {
      try {
        const svgContent = await Deno.readTextFile(`${dirPath}/${connector.icon}`);
        iconEntries.push([connector.name, svgContent]);
      } catch {
        // No SVG file for this connector
      }
    }
  } catch {
    // Skip directories without connector.json
  }
}

// Sort for deterministic output
connectorEntries.sort((a, b) => a[0].localeCompare(b[0]));
iconEntries.sort((a, b) => a[0].localeCompare(b[0]));

// Write _connectors.ts
const connectorMapEntries = connectorEntries
  .map(([name, config]) =>
    `  [${JSON.stringify(name)}, ${JSON.stringify(config)} as unknown as IntegrationConfig]`
  )
  .join(",\n");

await Deno.writeTextFile(
  connectorsPath,
  `// AUTO-GENERATED by scripts/build/generate-integrations-module.ts — do not hand-edit
import type { IntegrationConfig } from "./types.ts";

export const CONNECTORS: ReadonlyMap<string, IntegrationConfig> = new Map([
${connectorMapEntries},
]);
`,
);

// Write _icons.ts
const iconMapEntries = iconEntries
  .map(([name, svg]) => `  [${JSON.stringify(name)}, ${JSON.stringify(svg)}]`)
  .join(",\n");

await Deno.writeTextFile(
  iconsPath,
  `// AUTO-GENERATED by scripts/build/generate-integrations-module.ts — do not hand-edit

export const ICONS: ReadonlyMap<string, string> = new Map([
${iconMapEntries},
]);
`,
);

console.log(
  `\u2705 Generated ${connectorsPath} (${connectorEntries.length} connectors) and ${iconsPath} (${iconEntries.length} icons)`,
);
